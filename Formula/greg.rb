  homepage "https://github.com/orisilber/homebrew-greg"
  url "https://github.com/orisilber/homebrew-greg/archive/refs/tags/v1.3.0.tar.gz"
  sha256 "5055b68b4b6a07bda3ed1fe39db7b5cde6bfca81178a59a8d8505fcfbf50ce2a"
